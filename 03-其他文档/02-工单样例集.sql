	ON C.CUST_UID = S.CUST_UID
	AND C.DW_SNSH_DT= CURRENT_DATE -1
INNER JOIN PDM_VHIS.T01_TLCM_FRD_SSP_BUS_INF_FILE B
	ON B.EAC_ID = C.EAC_ID 
	AND B.REG_DT < S.COMPLAINT_TIME
LEFT JOIN AMIP_VHIS.AMIP_D_ORG_VAL D
	ON D.ORG_ID = B.REG_ORG_ID
	AND D.ORG_ID <> ''
GROUP BY 1,2,3,4,5,6,7,8
;


