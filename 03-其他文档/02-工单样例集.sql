WITH (ORIENTATION = COLUMN, COLVERSION = 2.0, COMPRESSION = MIDDLE) 
DISTRIBUTE  BY  HASH(EAC_ID)
AS
SELECT 
C.EAC_ID
,COALESCE(CAST(REPLACE(B.REG_DT,'-','') AS VARCHAR),'00010101') 管控日期
