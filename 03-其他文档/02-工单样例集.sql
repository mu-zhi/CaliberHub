case CHNL_TYP_CD when 'DSK' then '柜面'	when 'IEX' then '网银'	else CHNL_TYP_CD end as 渠道类型代码,
HDL_USR_ID 经办用户编号,HDL_DT 经办日期,OPR_TM 经办时间,CHK_USR_ID 复核用户编号,CHK_DT 复核日期,ESP_NO_20 "特殊码20",CNT_ASES_DONE 柜面做过评估,MRG_IND_CD 合并标志代码
FROM PDM_VHIS.T01_CUST_IRT_ASES_INF
WHERE CUST_ID = '***********'
ORDER BY ASES_START_DT;


--问卷及答案
SELECT distinct QST_CD 问卷代码,PROB_CD 问题代码,SYS_CD 系统代码,PROB_DSCR 问题描述
--QST_START_DT 问卷开始日期,QST_MTU_DT 问卷到期日期,DW_START_DT 开始日期,DW_END_DT 结束日期
FROM PDM_VHIS.T00_CUST_IRT_ASES_QST_PARM --客户投资风险承受能力评估问卷参数表
WHERE SYS_CD = 'FI' and QST_CD IN( '003','004')
ORDER BY 3,1,2;

SELECT ANS_CD 答案代码,PROB_CD 问题代码,SYS_CD 系统代码,QST_CD 问卷代码,ANS_DSCR 答案描述,ANS_BRF 答案简述,ANS_VAL 答案分值,REF_PROD_LVL_CD 参考产品级别代码,REF_RSK_ENDU_CD 参考风险承受代码
--,QST_START_DT 问卷开始日期,QST_MTU_DT 问卷到期日期,DW_START_DT 开始日期,DW_END_DT 结束日期
FROM PDM_VHIS.T00_CUST_IRT_ASES_ANS_PARM --客户投资风险承受能力评估问卷答案参数表
WHERE SYS_CD = 'FI' and QST_CD IN( '003','004')
AND QST_MTU_DT = '9999-12-31'
ORDER BY 3,4,2,1,5
;

